package qupath.ext.imglib2;

/**
 * Represent the physical interpretation of an accessible dimension.
 */
public enum Dimension {
    /**
     * X dimension.
     */
    X,
    /**
     * Y dimension.
     */
    Y,
    /**
     * Channel dimension.
     */
    CHANNEL,
    /**
     * Z dimension.
     */
    Z,
    /**
     * Time dimension
     */
    TIME
}
